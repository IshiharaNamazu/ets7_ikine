% パラメータの設定
A = 10;
T = 5;

% 時間ベクトル t の生成 (0からTまで0.01刻み)
t = 0:0.01:T;

% y の計算
% ベクトル演算に対応するため、乗算(*)とべき乗(^)の前にドット(.)を付けます
y = A/(T^3) * (3*T*t.^2 - 2*t.^3);
% Vmax = 3A/2T

% グラフのプロット
figure; % 新しいウィンドウでプロットを表示
plot(t, y, 'LineWidth', 2); % 線を太くして見やすくする

% グラフの装飾
grid on; % グリッドを表示
title('y = A/(T^3) * (3Tt^2 - 2t^3) のプロット'); % タイトル
xlabel('時間 t'); % x軸ラベル
ylabel('y'); % y軸ラベル
legend('y(t)'); % 凡例